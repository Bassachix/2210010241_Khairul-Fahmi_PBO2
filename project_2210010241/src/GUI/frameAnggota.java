    private void tabelAnggotaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelAnggotaMouseClicked
        // TODO add your handling code here:
        
        int row = tabelAnggota.getSelectedRow();

            if (row != -1) {
                String valueId = tabelAnggota.getValueAt(row, 0).toString();
                String valueNik = tabelAnggota.getValueAt(row, 1).toString();
                String valueNama = tabelAnggota.getValueAt(row, 2).toString();
                String valueAlamat = tabelAnggota.getValueAt(row, 3).toString();
                String valueTanggalLahir = tabelAnggota.getValueAt(row, 4).toString();
                String valueJenisKelamin = tabelAnggota.getValueAt(row, 5).toString();
                String valueStatus = tabelAnggota.getValueAt(row, 6).toString();

                textIdAnggota.setText(valueId);
                textNik.setText(valueNik);
                textNama.setText(valueNama);
                textAlamat.setText(valueAlamat);
                textTanggalLahir.setText(valueTanggalLahir);
                textJenisKelamin.setSelectedItem(valueJenisKelamin);
                textStatus.setSelectedItem(valueStatus);
            }
    }//GEN-LAST:event_tabelAnggotaMouseClicked

