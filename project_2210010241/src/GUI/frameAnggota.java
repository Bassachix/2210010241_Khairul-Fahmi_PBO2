    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:
        
        updateIsinya();
        
        if (Arrays.stream(isinya).anyMatch(String::isEmpty)) {
            JOptionPane.showMessageDialog(null, "Harap isi semua kolom terlebih dahulu jika ingin menggunakan tombol Simpan...!");
        } else {
            CRUD.SimpanDinamis(namaTabel, fieldnya, isinya);
            loadData();
            bersih();
        }
    }//GEN-LAST:event_buttonSimpanActionPerformed

    private void buttonUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUbahActionPerformed
        // TODO add your handling code here:
        
        String isiPrimary = textIdAnggota.getText();
        updateIsinya();
        
        if (isiPrimary.equals("")) {
            JOptionPane.showMessageDialog(null, "Harap isi 'Id Anggota' yang ingin diubah jika ingin menggunakan tombol Ubah...!");
            bersih();
            textIdAnggota.requestFocus();
        } else if (Arrays.stream(isinya).anyMatch(String::isEmpty)) {
            JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang kosong jika ingin menggunakan tombol Ubah...!");
        } else {
            CRUD.UbahDinamis(namaTabel, primaryKey, isiPrimary, fieldnya, isinya);
            loadData();
            bersih();
        }
    }//GEN-LAST:event_buttonUbahActionPerformed

    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        // TODO add your handling code here:
        
        String isiPrimary = textIdAnggota.getText();
        updateIsinya();
        
        if (isiPrimary.equals("")) {
            JOptionPane.showMessageDialog(null, "Mohon isi 'Id Anggota' yang ingin dihapus jika ingin menggunakan tombol Hapus...!");
            bersih();
            textIdAnggota.requestFocus();
        } else {
            CRUD.HapusDinamis(namaTabel, primaryKey, isiPrimary);
            loadData();
            bersih();
        }
    }//GEN-LAST:event_buttonHapusActionPerformed

